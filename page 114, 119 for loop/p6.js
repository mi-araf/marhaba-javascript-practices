// 1 থেকে 50 পর্যন্ত এমন সংখ্যাগুলো প্রিন্ট কর, যা 3 এবং 4 উভয়ের দ্বারা বিভাজ্য এবং সংখ্যা গুলো যোগফল বের কর।

let sum = 0;
for (let i = 1; i <= 50; i++) {
    if (i % 3 === 0 && i % 4 === 0) {
        console.log(i);
        sum += i;   // sum = sum + i;
    }
}
console.log('1 থেকে 50 পর্যন্ত এমন সংখ্যাগুলো, যা 3 এবং 4 উভয়ের দ্বারা বিভাজ্য এবং সংখ্যা গুলো যোগফল =', sum);